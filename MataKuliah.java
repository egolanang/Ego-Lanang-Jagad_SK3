

public class MataKuliah {
    private String mk_kode;
    private String mk_nama;
    private String mk_nilai;

    public String getMk_kode() {
        return mk_kode;
    }

    public void setMk_kode(String mk_kode) {
        this.mk_kode = mk_kode;
    }

    public String getMk_nama() {
        return mk_nama;
    }

    public void setMk_nama(String mk_nama) {
        this.mk_nama = mk_nama;
    }

    public String getMk_nilai() {
        return mk_nilai;
    }

    public void setMk_nilai(String mk_nilai) {
        this.mk_nilai = mk_nilai;
    }
}
